#include <stdio.h>

int main() {

    int x = ~0;     // 비트 NOT 연산자(~)를 사용하여 0의 모든 비트를 반전시킴
                    // 2의 보수 시스템에서는 결과가 -1이 됨
                    // 1의 보수 시스템에서는 결과가 -0이 됨

    if (x == -1) {                     // 결과가 -1이면
        printf("2의 보수입니다.\n");   // 현재 시스템이 2의 보수 방식임을 출력
    }
    else {                             // 결과가 -1이 아니라면
        printf("1의 보수입니다.\n");   // 1의 보수 방식이라고 출력
    }

    return 0;                         // 프로그램 정상 종료
}